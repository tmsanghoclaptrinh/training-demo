package com.cuonglamchi.model;

public class FunctionItem {
    // Attributes
    private int icon;
    private String name;

    // Constructor
    public FunctionItem(int icon, String name) {
        this.icon = icon;
        this.name = name;
    }

    // Getter
    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}
